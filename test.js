'use strict'

const client = require('./index')

client.init()
  .then(client.takeOff())
  .then(client.wait(5000))
  .then(client.moveForward(2000))
  .then(client.wait(5000))
  .then(client.moveBack(1000))
  .then(client.wait(5000))
  .then(client.moveRight(2000))
  .then(client.wait(5000))
  .then(client.moveLeft(2000))
  .then(client.wait(5000))
  .then(client.spinRight(2000))
  .then(client.wait(5000))
  .then(client.spinLeft(2000))
  .then(client.wait(5000))
  .then(client.moveUp(2000))
  .then(client.wait(5000))
  .then(client.moveDown(2000))
  .then(client.wait(5000))
  .then(client.land())